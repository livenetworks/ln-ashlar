// ==========================================================================
// ln-ashlar SPA starter — module-concatenation build
// ==========================================================================
// Authoring model: one folder per module under src/, each holding up to three
// files that share the folder name's intent — a .html snippet, a .js
// coordinator, and an optional .scss partial.
//
// This script stitches the modules back into a single deployable app:
//   • JS   — every src/<module>/*.js concatenated into dist/app.js
//   • HTML — every snippet bucketed by `<!-- @zone X -->` and injected into
//            src/index.template.html → index.html
//   • CSS  — module .scss files aggregated and compiled via Dart Sass
//
// No per-file bundling: the .js files are plain, self-contained scripts that
// only use the public globals exposed by the ln-ashlar bundle, so a literal
// concatenation is all that is needed.
// ==========================================================================

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import * as sass from 'sass-embedded';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.join(__dirname, 'src');
const DIST = path.join(__dirname, 'dist');
const OUT_HTML = path.join(__dirname, 'index.html');
const TEMPLATE = path.join(SRC, 'index.template.html');
const GENERATED_SCSS = path.join(SRC, '_modules.generated.scss');

const ZONES = ['shell', 'persistent', 'routes'];

// Module folders, sorted. '_' sorts before lowercase letters, so a folder like
// `_core` (the runtime) is always emitted first.
function moduleDirs() {
	return fs.readdirSync(SRC, { withFileTypes: true })
		.filter((d) => d.isDirectory())
		.map((d) => d.name)
		.sort();
}

function filesIn(dir, ext) {
	const full = path.join(SRC, dir);
	return fs.readdirSync(full)
		.filter((f) => f.endsWith(ext))
		.sort()
		.map((f) => path.join(full, f));
}

const rel = (file) => path.relative(SRC, file).split(path.sep).join('/');

// ── JS: concatenate every module's scripts into one app.js ───────────────
function buildJs() {
	const parts = [];
	for (const dir of moduleDirs()) {
		for (const file of filesIn(dir, '.js')) {
			parts.push(`/* ── module: ${rel(file)} ─────────────────────────── */\n` +
				fs.readFileSync(file, 'utf8').trim());
		}
	}
	fs.mkdirSync(DIST, { recursive: true });
	fs.writeFileSync(path.join(DIST, 'app.js'), parts.join('\n\n') + '\n');
}

// ── HTML: split a snippet into zones on `<!-- @zone X -->` markers ───────
// Content before the first marker defaults to the `routes` zone.
function splitZones(html, source) {
	const out = { shell: '', persistent: '', routes: '' };
	const re = /<!--\s*@zone\s+(\w+)\s*-->/g;
	let current = 'routes';
	let last = 0;
	let m;
	const push = (zone, text) => {
		if (!(zone in out)) {
			throw new Error(`${source}: unknown @zone "${zone}" (expected ${ZONES.join(' | ')})`);
		}
		out[zone] += text;
	};
	while ((m = re.exec(html)) !== null) {
		push(current, html.slice(last, m.index));
		current = m[1];
		last = re.lastIndex;
	}
	push(current, html.slice(last));
	return out;
}

function buildHtml() {
	const buckets = { shell: '', persistent: '', routes: '' };
	for (const dir of moduleDirs()) {
		for (const file of filesIn(dir, '.html')) {
			const zoned = splitZones(fs.readFileSync(file, 'utf8'), rel(file));
			for (const z of ZONES) {
				const chunk = zoned[z].trim();
				if (chunk) buckets[z] += `\n\t\t<!-- ${rel(file)} -->\n\t\t${chunk}\n`;
			}
		}
	}
	let tpl = fs.readFileSync(TEMPLATE, 'utf8');
	for (const z of ZONES) {
		const marker = `<!-- @inject ${z} -->`;
		if (!tpl.includes(marker)) throw new Error(`index.template.html is missing "${marker}"`);
		tpl = tpl.replace(marker, buckets[z].trimEnd());
	}
	fs.writeFileSync(OUT_HTML, tpl);
}

// ── CSS: aggregate module styles, compile the single entry ───────────────
function buildCss() {
	const uses = [];
	for (const dir of moduleDirs()) {
		for (const file of filesIn(dir, '.scss')) {
			uses.push(`@use '${rel(file).replace(/\.scss$/, '')}';`);
		}
	}
	fs.writeFileSync(GENERATED_SCSS,
		'// AUTO-GENERATED by build.mjs — do not edit.\n' + uses.join('\n') + '\n');

	const result = sass.compile(path.join(SRC, 'app.scss'), { style: 'compressed' });
	fs.mkdirSync(DIST, { recursive: true });
	fs.writeFileSync(path.join(DIST, 'app.css'), result.css);
}

function build() {
	const t0 = Date.now();
	buildJs();
	buildHtml();
	buildCss();
	console.log(`[spa-starter] built in ${Date.now() - t0}ms → ${path.relative(process.cwd(), OUT_HTML)}`);
}

build();

if (process.argv.includes('--watch')) {
	console.log('[spa-starter] watching src/ for changes…');
	let pending = null;
	fs.watch(SRC, { recursive: true }, (_event, filename) => {
		if (filename && filename.includes('_modules.generated')) return; // ignore own output
		clearTimeout(pending);
		pending = setTimeout(() => {
			try { build(); } catch (err) { console.error('[spa-starter]', err.message); }
		}, 100);
	});
}
